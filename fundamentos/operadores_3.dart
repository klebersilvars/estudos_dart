main() {
  //operadores de incrementação ou decrementação
  int a = 2;
  a = a+1;

  int b= 3;
  b++;

  print(a); //o resultado aqui será de 3
  print(b); // o resultado aqui será de 4

  //decrementando algum valor

  int c = 4;
  int d = 6;
  c--;
  d = d-1;
  print(c); // o valor aqui será de 3
  print(d); // o valor aqui será de 5
}